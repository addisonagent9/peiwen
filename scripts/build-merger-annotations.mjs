#!/usr/bin/env node
/**
 * Build script for #7 — 簡↔繁 rhyme-merger annotations.
 *
 * Reads src/data/pingshui.json + uses opencc-js (cn/tw mode) to find
 * simp chars that merge multiple trad forms with classically-distinct
 * 平水韻 rhyme placements. Emits src/data/merger-annotations.ts.
 *
 * Filter pipeline (verified in Part 1):
 *   Step A — group pingshui chars by their opencc t2s output.
 *            Keep groups where ≥2 trad forms share the same simp.
 *   Step B — among Step A groups, keep only when the FAMILY of
 *            rhyme-sets across trads has ≥2 distinct members
 *            (i.e., at least two trads have DIFFERENT rhyme membership).
 *            This drops trivial cases where all trads share identical
 *            rhyme sets and the multi-tone pattern is per-trad
 *            multi-reading rather than a real classical merger.
 *
 * Step B's specific check:
 *   serialize each trad's unique rhyme set as a sorted-joined key,
 *   take the unique set of those keys, require cardinality ≥ 2.
 *
 * Determinism:
 *   - Object.keys(pingshui.chars) traversal order is stable in V8
 *     (insertion order for string keys).
 *   - Final sort by simp codepoint provides cross-engine stability.
 *   - Within each entry, trad forms sorted by codepoint.
 *   - Within each trad, rhymes/tones sorted alphabetically.
 *
 * Manual invocation only — NOT chained into npm run data.
 *
 * Usage:
 *   node scripts/build-merger-annotations.mjs
 *
 * Output: src/data/merger-annotations.ts (overwrites if exists).
 */

import path from 'path';
import { fileURLToPath } from 'url';
import { createRequire } from 'module';
import { readFileSync, writeFileSync } from 'fs';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const require = createRequire(import.meta.url);

const PINGSHUI_PATH = path.join(__dirname, '..', 'src', 'data', 'pingshui.json');
const OUTPUT_PATH = path.join(__dirname, '..', 'src', 'data', 'merger-annotations.ts');

// opencc-js loaded from repo node_modules. Use the ESM 'full.js' build.
const opencc = require('opencc-js');
const t2s = opencc.Converter({ from: 'tw', to: 'cn' });
const s2t = opencc.Converter({ from: 'cn', to: 'tw' });

// 平水韻 tone → category. Pingshui entries store both `tone` (平/仄/入) and
// `group` (上平/下平/上聲/去聲/入聲). For the annotation's `tones` field we
// surface the 3-way 平/仄/入 distinction.
function normalizeTone(toneRaw) {
  // pingshui values: '平' or '仄' (with 入 cases mapped to 仄 in some sources,
  // or '入' as its own value depending on the build). Check both.
  if (toneRaw === '平') return '平';
  if (toneRaw === '入') return '入';
  return '仄';
}

function rhymesOf(ps, trad) {
  return [...new Set((ps.chars[trad] ?? []).map(e => e.rhyme))].sort();
}

function tonesOf(ps, trad) {
  const set = new Set();
  for (const e of (ps.chars[trad] ?? [])) {
    set.add(normalizeTone(e.tone));
  }
  // Order: 平 → 入 → 仄 (visual preference; tone glyphs not codepoint-sorted)
  const order = ['平', '入', '仄'];
  return [...set].sort((a, b) => order.indexOf(a) - order.indexOf(b));
}

function buildMergers() {
  const ps = JSON.parse(readFileSync(PINGSHUI_PATH, 'utf-8'));

  // Step A — group by opencc t2s output
  const simpToTrads = new Map();
  for (const trad of Object.keys(ps.chars)) {
    const simp = t2s(trad);
    if (!simpToTrads.has(simp)) simpToTrads.set(simp, []);
    simpToTrads.get(simp).push(trad);
  }
  const stepA = [...simpToTrads.entries()].filter(([_, trads]) => trads.length >= 2);

  // Step B (refined) — keep only when trads have DIFFERENT rhyme membership
  const candidates = [];
  for (const [simp, trads] of stepA) {
    const rhymeSetByTrad = new Map();
    for (const t of trads) rhymeSetByTrad.set(t, rhymesOf(ps, t));
    const uniqueRhymeSetKeys = new Set(
      [...rhymeSetByTrad.values()].map(rs => rs.join('|'))
    );
    if (uniqueRhymeSetKeys.size < 2) continue;

    // Build entry — trads sorted by codepoint for stability
    const sortedTrads = [...trads].sort(
      (a, b) => a.codePointAt(0) - b.codePointAt(0)
    );
    const openccDefault = s2t(simp);
    const forms = sortedTrads.map(trad => ({
      trad,
      rhymes: rhymeSetByTrad.get(trad),
      tones: tonesOf(ps, trad),
      isOpenccDefault: trad === openccDefault,
    }));
    candidates.push({ simp, openccDefault, forms });
  }

  // Sort by simp codepoint
  candidates.sort((a, b) => a.simp.codePointAt(0) - b.simp.codePointAt(0));

  return candidates;
}

function emitTypeScript(annotations) {
  const lines = [];
  lines.push('// AUTO-GENERATED by scripts/build-merger-annotations.mjs');
  lines.push('// Do not edit by hand. Re-run script after pingshui.json changes.');
  lines.push('//');
  lines.push('// Source: src/data/pingshui.json + opencc-js (cn/tw mode).');
  lines.push('// Filter: simp→multi-trad where trads have classically-distinct');
  lines.push('// 平水韻 rhyme placements (丰/豐 pattern). See #7 ticket / Part 1');
  lines.push('// design for the precision-tuned filter pipeline.');
  lines.push('');
  lines.push('export interface MergerForm {');
  lines.push('  trad: string;');
  lines.push('  /** sorted unique 平水韻 rhyme labels this trad appears in */');
  lines.push('  rhymes: string[];');
  lines.push('  /** sorted unique tone categories (平 / 入 / 仄) */');
  lines.push('  tones: ReadonlyArray<\'平\' | \'仄\' | \'入\'>;');
  lines.push('  /** Optional gloss; empty in this generation. Future ticket may auto-pop from MOE. */');
  lines.push('  meaning?: string;');
  lines.push('  /** True if this trad is what `opencc t2s(simp)` returns (the analyzer\'s current default). */');
  lines.push('  isOpenccDefault: boolean;');
  lines.push('}');
  lines.push('');
  lines.push('export interface MergerAnnotation {');
  lines.push('  /** the merged simp form (also the Record key) */');
  lines.push('  simp: string;');
  lines.push('  /** 2+ trad forms with their classical placements; sorted by codepoint */');
  lines.push('  forms: MergerForm[];');
  lines.push('  /** what `opencc t2s(simp)` returns; duplicates one form\'s `trad` for fast lookup */');
  lines.push('  openccDefault: string;');
  lines.push('  /** Optional human-curated note; empty in this generation. Render only when populated. */');
  lines.push('  pedagogicalNote?: string;');
  lines.push('}');
  lines.push('');
  lines.push('/** Map of simp char → annotation. O(1) lookup. */');
  lines.push('export const MERGER_ANNOTATIONS: Record<string, MergerAnnotation> = {');

  for (const a of annotations) {
    const formsLines = a.forms.map(f => {
      const tonesStr = f.tones.map(t => `'${t}'`).join(', ');
      const rhymesStr = f.rhymes.map(r => `'${r}'`).join(', ');
      return `    { trad: '${f.trad}', rhymes: [${rhymesStr}], tones: [${tonesStr}], isOpenccDefault: ${f.isOpenccDefault} },`;
    });
    lines.push(`  '${a.simp}': {`);
    lines.push(`    simp: '${a.simp}',`);
    lines.push(`    openccDefault: '${a.openccDefault}',`);
    lines.push(`    forms: [`);
    for (const fl of formsLines) lines.push(`  ${fl}`);
    lines.push(`    ],`);
    lines.push(`  },`);
  }

  lines.push('};');
  lines.push('');
  lines.push('/**');
  lines.push(' * Auto-generated default note when curated `pedagogicalNote` is empty.');
  lines.push(' * Format: "[simp] 在平水韻中可指 [trad1] ([rhyme1]) 或 [trad2] ([rhyme2]) ..."');
  lines.push(' *');
  lines.push(' * Not auto-rendered in EditModal/RhymeCharCard banners — call sites that');
  lines.push(' * want a fallback note when `pedagogicalNote` is empty can use this.');
  lines.push(' */');
  lines.push('export function defaultNote(annotation: MergerAnnotation): string {');
  lines.push('  const parts = annotation.forms.map((f) => `${f.trad} (${f.rhymes.join(\' / \')})`);');
  lines.push('  return `${annotation.simp} 在平水韻中可指 ${parts.join(\' 或 \')}`;');
  lines.push('}');
  lines.push('');

  return lines.join('\n');
}

function main() {
  const annotations = buildMergers();
  const ts = emitTypeScript(annotations);
  writeFileSync(OUTPUT_PATH, ts, 'utf-8');
  console.log(`[merger-annotations] wrote ${annotations.length} entries to ${path.relative(path.join(__dirname, '..'), OUTPUT_PATH)}`);
}

main();
